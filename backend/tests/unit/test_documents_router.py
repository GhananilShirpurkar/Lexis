def test_upload_document_db_error_rollback(client, mock_db):
    """
    Test document upload when database insertion fails.
    Verifies that the uploaded storage object and local index files are correctly cleaned up.
    """
    user_uuid = uuid.uuid4()
    mock_payload = {
        "sub": str(user_uuid),
        "email": "test@example.com"
    }

    mock_summary = "This is a document summary."
    mock_index_result = {
        "summary": mock_summary,
        "text": "Parsed file content text",
        "index": MagicMock()
    }

    # Make db commit raise an exception
    mock_db.commit.side_effect = Exception("DB Connection Error")

    with patch("app.auth.middleware.decode_token", return_value=mock_payload), \
         patch("app.routers.documents.upload_file", return_value=f"{user_uuid}/mock-doc-id/test.txt") as mock_upload, \
         patch("app.routers.documents.delete_file") as mock_delete, \
         patch("app.routers.documents.index_document", return_value=mock_index_result), \
         patch("shutil.rmtree") as mock_rmtree, \
         patch("os.path.exists", return_value=True):

        files = {"file": ("test.txt", b"Hello world text document", "text/plain")}
        headers = {"Authorization": "Bearer dummy-token"}
        
        with pytest.raises(Exception, match="DB Connection Error"):
            client.post("/documents/upload", files=files, headers=headers)

        # Verify that delete_file was called to clean up storage
        mock_delete.assert_called_once()
        # Verify that local index folder was cleaned up
        mock_rmtree.assert_called_once()
        mock_db.rollback.assert_called_once()

