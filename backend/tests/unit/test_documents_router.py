def test_upload_document_success(client, mock_db):
    """
    Test successful document upload.
    Verifies that with a valid JWT token, the route:
    1. Validates size/type.
    2. Uploads the file to storage.
    3. Indexes the document.
    4. Saves the Document model to NeonDB.
    5. Returns a 201 Created status and a valid DocumentResponse metadata payload.
    """
    user_uuid = uuid.uuid4()
    mock_payload = {
        "sub": str(user_uuid),
        "email": "test@example.com"
    }

    mock_summary = "This is a document summary generated by LLM."
    mock_index_result = {
        "summary": mock_summary,
        "text": "Parsed file content text",
        "index": MagicMock()
    }

    # Patch the JWT verification middleware and the backend upload/index functions
    with patch("app.auth.middleware.decode_token", return_value=mock_payload), \
         patch("app.routers.documents.upload_file", return_value=f"{user_uuid}/mock-doc-id/test.txt") as mock_upload, \
         patch("app.routers.documents.index_document", return_value=mock_index_result) as mock_index:

        files = {"file": ("test.txt", b"Hello world text document", "text/plain")}
        headers = {"Authorization": "Bearer dummy-token"}
        
        response = client.post("/documents/upload", files=files, headers=headers)
        
        assert response.status_code == status.HTTP_201_CREATED
        data = response.json()
        assert "id" in data
        assert data["user_id"] == str(user_uuid)
        assert data["filename"] == "test.txt"
        assert data["size_bytes"] == len(b"Hello world text document")
        assert data["status"] == "completed"
        assert data["summary"] == mock_summary
        assert "uploaded_at" in data
        assert "expiry_at" in data

        # Verify downstream upload & indexing were triggered with correct parameters
        mock_upload.assert_called_once()
        called_args = mock_upload.call_args[1]
        assert called_args["user_id"] == user_uuid
        assert called_args["filename"] == "test.txt"
        assert called_args["data"] == b"Hello world text document"

        mock_index.assert_called_once()
        index_args = mock_index.call_args[1]
        assert index_args["user_id"] == user_uuid
        assert index_args["filename"] == "test.txt"
        assert index_args["file_bytes"] == b"Hello world text document"

        # Verify database session calls
        mock_db.add.assert_called_once()
        mock_db.commit.assert_called_once()

